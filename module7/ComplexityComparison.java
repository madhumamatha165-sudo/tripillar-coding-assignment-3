public class ComplexityComparison {

    public static void main(String[] args) {

        System.out.println("Sorting Algorithm Complexity Comparison\n");

        System.out.println("Bubble Sort:");
        System.out.println("Best Case: O(n)");
        System.out.println("Average Case: O(n^2)");
        System.out.println("Worst Case: O(n^2)\n");

        System.out.println("Selection Sort:");
        System.out.println("Best Case: O(n^2)");
        System.out.println("Average Case: O(n^2)");
        System.out.println("Worst Case: O(n^2)\n");

        System.out.println("Insertion Sort:");
        System.out.println("Best Case: O(n)");
        System.out.println("Average Case: O(n^2)");
        System.out.println("Worst Case: O(n^2)\n");

        System.out.println("Merge Sort:");
        System.out.println("Best Case: O(n log n)");
        System.out.println("Average Case: O(n log n)");
        System.out.println("Worst Case: O(n log n)\n");

        System.out.println("Quick Sort:");
        System.out.println("Best Case: O(n log n)");
        System.out.println("Average Case: O(n log n)");
        System.out.println("Worst Case: O(n^2)");
    }
}
